myList = [0, 3, 12, 8, 2]

print(5 in myList) # 5 is not in "myList" = False
print(5 not in myList) # 5 is not in "myList" = True
print(12 in myList) # 12 is in "myList" = True

# in: If an element is in a list then in operator becomes True.
# in: If an element is n't in a list then in operator becomes False.
# not in: If an element is in a list, then not in operator becomes False.
# not in: If an element is n't in a list, then not in operator becomes True.